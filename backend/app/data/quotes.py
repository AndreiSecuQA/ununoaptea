# --------------------------------------------------------- AGGREGATE ----
ALL_QUOTES_LIST: list[Quote] = [
    *STOIC,
    *MODERN,
    *SPIRITUAL,
    *ROMANIAN,
    *EXISTENTIALIST,
]


def _build_pool_index(quotes: list[Quote]) -> dict[str, list[Quote]]:
    """{pool_name: [all quotes tagged with that pool]}."""
    pools: dict[str, list[Quote]] = {
        "productive": [],
        "rest": [],
        "reflection": [],
        "celebration": [],
        "general": [],
    }
    for q in quotes:
        for p in q.get("pools", []):
            if p in pools:
                pools[p].append(q)
    return pools


ALL_QUOTES: dict[str, list[Quote]] = _build_pool_index(ALL_QUOTES_LIST)


def get_pools_for_styles(styles: Sequence[str]) -> dict[str, list[Quote]]:
    """Filter the global pools by the user's selected styles.

    If the filtered pool for some category is empty (e.g. user picked only
    'existentialist' which has no 'celebration' quote), fall back to the
    unfiltered pool for that category so the renderer always has content.
    """
    style_set = set(styles)
    filtered: dict[str, list[Quote]] = {
        pool: [q for q in quotes if q["style"] in style_set]
        for pool, quotes in ALL_QUOTES.items()
    }
    # Fallback: any empty pool inherits from the full pool.
    for pool in filtered:
        if not filtered[pool]:
            filtered[pool] = list(ALL_QUOTES[pool]) or list(ALL_QUOTES_LIST)
    return filtered
